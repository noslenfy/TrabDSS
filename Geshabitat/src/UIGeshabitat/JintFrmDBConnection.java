    private void jBtSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSaveActionPerformed
        DBConnection conn = new DBConnection("mysql",jTxtHost.getText(),jTxtPort.getText(),jTxtUser.getText(),jTxtPass.getText(),jTxtDatabaseName.getText());
        try {
           conn.save(JmdiMain.configFile);
        } catch (IOException ex) {
           JOptionPane.showMessageDialog(this,"Não foi possivel guardar as configurações" + ex.getMessage(),"Erro", JOptionPane.ERROR_MESSAGE);
           return;
        }
        JOptionPane.showMessageDialog(this,"As configuracoes foram guardadas com sucesso, por favor reinice o programa","Sucesso", JOptionPane.PLAIN_MESSAGE);
        System.exit(0);

    }//GEN-LAST:event_jBtSaveActionPerformed

    private void jBtTestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtTestActionPerformed
        DBConnection conn = new DBConnection("mysql",jTxtHost.getText(),jTxtPort.getText(),jTxtUser.getText(),jTxtPass.getText(),jTxtDatabaseName.getText());
        try {
            FacadeDAO dummyFacade = new FacadeDAO(conn);
            JOptionPane.showMessageDialog(this,"Teste efectuada com sucesso!\nPode guardar configurações","Sucesso", JOptionPane.PLAIN_MESSAGE);
            dummyFacade.closeConnection();
        } catch (PersistableException ex) {
           JOptionPane.showMessageDialog(this,"Não foi possivel estabelecer conexão!","Erro", JOptionPane.ERROR_MESSAGE);
        }
        
        
        
        
    }//GEN-LAST:event_jBtTestActionPerformed


