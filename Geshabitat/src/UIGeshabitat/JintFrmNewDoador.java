    private void jBtAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtAddActionPerformed
        int Nif=-1;
        
        try{
        Nif = Integer.valueOf(jTxtNif.getText());
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this,"O n√∫mero de contrinuinte que inseriu n√£o √© v√°lido","Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String Telefone = jTxtTelefone.getText();
        boolean Parceria = jChkParceria.isSelected();
        String Tipo = (String)jCmbTipo.getSelectedItem();
        String Rua = jTxtRua.getText();
        String Email = jTxtEmail.getText();
        String Localidade = jTxtLocalidade.getText();
        String Nome = jTxtNome.getText();
        String Cp = jTxtCp.getText();
        
        
        String EstadoCivil;
        String Profissao;
        java.sql.Date DtNascimento;
        String Actividade;
        if(Tipo.equals("Particular")) {
             DtNascimento = new java.sql.Date(jDpDtNascimento.getDate().getTime());
             Profissao = jTxtProfissao.getText();
             EstadoCivil = (String)jCmbEstadoCivil.getSelectedItem();
             Actividade = null;
        } else {
            Actividade = jTxtActividade.getText();
            DtNascimento = null;
            Profissao = null;
            EstadoCivil = null;
        }
   
        Doador doador = new Doador(Nif,Nome, Telefone,Email, Rua, Localidade, Cp, DtNascimento, EstadoCivil, Profissao, Actividade, Tipo, Parceria);
        

 
        try {
            JmdiMain.facadeBL.put(doador);
            
        } catch (PersistableException ex) {
            JOptionPane.showMessageDialog(this,"Ocorreu um erro a cria√ß√£o do registo","Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        JOptionPane.showMessageDialog(this,"Registo adicionado com sucesso!","Sucesso", JOptionPane.INFORMATION_MESSAGE);

        this.dispose();
    }//GEN-LAST:event_jBtAddActionPerformed

